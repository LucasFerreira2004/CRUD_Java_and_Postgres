    private void JbDeletarFuncionarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbDeletarFuncionarioActionPerformed
        String MatriculaFuncionarioString = JtfMatriculaFuncionario.getText();
        ControleGerente manGerente = null;  
      
        boolean camposPreenchidos = !(MatriculaFuncionarioString.isEmpty());
        if (camposPreenchidos) {
            int matriculaFuncionario = Integer.parseInt(MatriculaFuncionarioString);
            
             manGerente = new ControleGerente();
            if(manGerente.getFuncionarioById(matriculaFuncionario) != null){
                manGerente.deleteFuncionario(matriculaFuncionario); 
            } else {
                JOptionPane.showMessageDialog(null, "Identificador n√£o encontrado, verifique os dados e tente novamente.");
            }  
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para Atualizar.");
        }
    }//GEN-LAST:event_JbDeletarFuncionarioActionPerformed

