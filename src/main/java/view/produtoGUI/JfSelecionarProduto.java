    private void JbSelecionarProdutoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_JbSelecionarProdutoActionPerformed
        String idProdutoString = JtfIdProduto.getText();
        ControleFuncionario manFuncionario = new ControleFuncionario();
        
        boolean camposPreenchidos = !(idProdutoString.isEmpty());
        if (camposPreenchidos) {
            int idProduto = Integer.parseInt(idProdutoString);
            if(manFuncionario.getProdutoById(idProduto) != null){
                String mensagemSucesso = "Produto: " + manFuncionario.getProdutoById(idProduto).describeProduto() + "\nAcessado com sucesso";
            JOptionPane.showMessageDialog(null, mensagemSucesso);                 
            } else {
                JOptionPane.showMessageDialog(null, "Identificador n√£o encontrado, verifique os dados e tente novamente.");
            }  
        } else {
            JOptionPane.showMessageDialog(null, "Preencha todos os campos para Selecionar.");
        }
    }//GEN-LAST:event_JbSelecionarProdutoActionPerformed

